def list_categories() -> list[dict]:
    return PALANGAL_CATEGORIES


def list_articles(category_id: str) -> list[dict]:
    items = PALANGAL_ARTICLES.get(category_id, [])
    return [{"category_id": category_id, **a} for a in items]


def get_article(category_id: str, article_id: int) -> dict | None:
    for a in PALANGAL_ARTICLES.get(category_id, []):
        if a["id"] == article_id:
            return {"category_id": category_id, **a}
    return None
