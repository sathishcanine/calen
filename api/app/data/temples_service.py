def _ensure_dirs() -> None:
    IMAGES_DIR.mkdir(parents=True, exist_ok=True)


def _ext_from_url(url: str, content_type: str) -> str:
    path = urlparse(url).path.lower()
    for ext in (".jpg", ".jpeg", ".png", ".webp"):
        if path.endswith(ext):
            return ext
    if "png" in content_type:
        return ".png"
    if "webp" in content_type:
        return ".webp"
    return ".jpg"


def _fetch_wikipedia_image(wiki_titles: tuple[str, ...]) -> tuple[str, str]:
    """Return remote image URL and Wikipedia page URL for the first matching article."""
    last_error: Exception | None = None
    for wiki_title in wiki_titles:
        try:
            resp = requests.get(
                _WIKI_API,
                params={
                    "action": "query",
                    "titles": wiki_title,
                    "prop": "pageimages|info",
                    "piprop": "original",
                    "inprop": "url",
                    "format": "json",
                },
                headers={"User-Agent": _USER_AGENT},
                timeout=25,
            )
            resp.raise_for_status()
            pages = resp.json().get("query", {}).get("pages", {})
            page = next(iter(pages.values()), {})
            if page.get("missing"):
                continue

            source_url = page.get("fullurl") or f"https://en.wikipedia.org/wiki/{wiki_title.replace(' ', '_')}"
            original = (page.get("original") or {}).get("source", "")
            if original:
                return original, source_url

            thumb = (page.get("thumbnail") or {}).get("source", "")
            if thumb:
                return thumb, source_url
        except Exception as exc:
            last_error = exc
            continue

    if last_error:
        raise last_error
    raise RuntimeError(f"No image on Wikipedia pages: {', '.join(wiki_titles)}")


def _download_image(remote_url: str, slug: str) -> str:
    """Download image to local storage; return stored filename."""
    _ensure_dirs()
    resp = requests.get(
        remote_url,
        headers={"User-Agent": _USER_AGENT},
        timeout=40,
    )
    resp.raise_for_status()
    ext = _ext_from_url(remote_url, resp.headers.get("content-type", ""))
    filename = f"{slug}{ext}"
    (IMAGES_DIR / filename).write_bytes(resp.content)
    return filename



def sync_temples(db: Session) -> int:
    """Upsert curated famous temples and refresh locally stored images."""
    _ensure_dirs()
    synced = 0
    curated_slugs = {seed.slug for seed in _CURATED_TEMPLES}
    for row in db.query(Temple).filter(~Temple.slug.in_(curated_slugs)).all():
        db.delete(row)

    for seed in _CURATED_TEMPLES:
        row = db.query(Temple).filter(Temple.slug == seed.slug).first()
        if row is None:
            row = Temple(slug=seed.slug)
            db.add(row)

        row.name_ta = seed.name_ta
        row.name_en = seed.name_en
        row.location_ta = seed.location_ta
        row.deity_ta = seed.highlights_ta
        row.description_ta = seed.description_ta
        row.sort_order = seed.sort_order
        row.is_featured = seed.is_featured
        row.source_label = "Wikipedia"

        try:
            remote_url, source_url = _fetch_wikipedia_image(seed.wiki_titles)
            filename = _download_image(remote_url, seed.slug)
            row.image_url = filename
            row.source_url = source_url
        except Exception as exc:
            print(f"Temple image skipped ({seed.slug}): {exc}")
            if not row.image_url:
                row.source_url = f"https://en.wikipedia.org/wiki/{seed.wiki_titles[0].replace(' ', '_')}"

        synced += 1

    db.commit()
    return synced


def list_temples(db: Session, limit: int = 30) -> list[Temple]:
    return (
        db.query(Temple)
        .order_by(Temple.is_featured.desc(), Temple.sort_order.asc(), Temple.id.asc())
        .limit(limit)
        .all()
    )
